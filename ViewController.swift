import UIKit

class ViewController: UIViewController {

    @IBAction func redRainbow(_ sender: Any) {
        view.backgroundColor = .red
    }
    
    @IBAction func orangeRainbow(_ sender: Any) {
        view.backgroundColor = .orange
    }
    
    @IBAction func yellowRainbow(_ sender: Any) {
        view.backgroundColor = .yellow
    }
    
    @IBAction func greenRainbow(_ sender: Any) {
        view.backgroundColor = .green
    }
    
    @IBAction func blueRainbow(_ sender: Any) {
        view.backgroundColor = .blue
    }
    
    @IBAction func purpleRainbow(_ sender: Any) {
        view.backgroundColor = .purple
    }
}




